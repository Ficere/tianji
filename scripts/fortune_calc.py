# 格式：{五行局数: {余数: (宫位地支索引的偏移规律)}}
# 算法：农历日 ÷ 局数，得商q和余数r
#       先从寅宫(idx=2)顺数q*局数步，再按余数查偏移
# 实际采用更直接的查表法：给定五行局和农历生日，查紫微所在宫位（相对子宫的地支索引）
# 此表基于《紫微斗数全书》安紫微法：
#   水二局：1→申，2→子
#   木三局：1→亥，2→卯，3→未（循环）
#   金四局：1→寅，2→午，3→戌，4→（进位）
