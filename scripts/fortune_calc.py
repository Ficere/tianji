    h = hour_float
    if h >= 23 or h < 1: return 0
    elif h < 3: return 1
    elif h < 5: return 2
    elif h < 7: return 3
    elif h < 9: return 4
    elif h < 11: return 5
    elif h < 13: return 6
    elif h < 15: return 7
    elif h < 17: return 8
    elif h < 19: return 9
    elif h < 21: return 10
    else: return 11


